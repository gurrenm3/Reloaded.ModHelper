namespace Reloaded.ModHelper
{
    /// <summary>
    /// Mouse codes for User32 Mouse methods
    /// </summary>
    public enum MouseButton : int
    {
        /// <summary>
        /// Left mouse button
        /// </summary>
        Left = 0x01,

        /// <summary>
        /// Right mouse button
        /// </summary>
        Right = 0x02,

        /// <summary>
        /// Middle mouse button
        /// </summary>
        Middle = 0x04,

        /// <summary>
        /// Back button?
        /// </summary>
        XButton1 = 0x05,

        /// <summary>
        /// Forward button?
        /// </summary>
        XButton2 = 0x06,
    }
}
