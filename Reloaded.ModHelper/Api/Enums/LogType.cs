namespace Reloaded.ModHelper
{
    /// <summary>
    /// Used to change how a message is logged.
    /// </summary>
    public enum LogType
    {
        /// <summary>
        /// WriteLine this message as a normal message.
        /// </summary>
        Normal,

        /// <summary>
        /// WriteLine this message as a warning.
        /// </summary>
        Warning,

        /// <summary>
        /// WriteLine this message as an error.
        /// </summary>
        Error
    }
}
